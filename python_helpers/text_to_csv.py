def parse_log(text):
    """
    Extracts Test number, Solve Time (ms), and Error (mm) from the log text.
    The expected log format should include lines like:
      Test: <number>
      Solve Time (ms): <value>
      Error (mm): <value>
    """
    # Remove any content inside square brackets along with the brackets
    cleaned_text = re.sub(r'\[.*?\]\s*', '', text)

    # Patterns for each field
    test_pattern   = re.compile(r"Test:\s*(\d+)")
    solve_pattern  = re.compile(r"Solve Time \(ms\):\s*([\d\.eE+-]+)")
    error_pattern  = re.compile(r"Error \(mm\):\s*([\d\.eE+-]+)")

    tests = test_pattern.findall(cleaned_text)
    solves = solve_pattern.findall(cleaned_text)
    errors = error_pattern.findall(cleaned_text)

    if not (len(tests) == len(solves) == len(errors)):
        print("Warning: The number of tests, solve times, and errors do not match exactly.")

    rows = []
    for i in range(min(len(tests), len(solves), len(errors))):
        rows.append((tests[i], solves[i], errors[i]))
    return rows

